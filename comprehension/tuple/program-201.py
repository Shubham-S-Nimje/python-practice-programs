# Creating tuple with one value or item is called singleton tuple
# Singleton tuple is create using (item,) or item,

# >>> t2=(10)
# >>> print(t2,type(t2))
# 10 <class 'int'>
# >>> t3=(10,)
# >>> print(t3,type(t3))
# (10,) <class 'tuple'>
# >>> t4=10,
# >>> print(t4,type(t4))
# (10,) <class 'tuple'>

