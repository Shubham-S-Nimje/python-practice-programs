a=10 # Global Variable
b=5  # Global Variable

def add():
    print(f'Sum of {a} and {b} is {a+b}')
def sub():
    print(f'Diff of {a} and {b} is {a-b}')
def multiply():
    print(f'Product of {a} and {b} is {a*b}')
def div():
    print(f'Division of {a} and {b} is {a/b}')


add()
sub()
multiply()
div()
